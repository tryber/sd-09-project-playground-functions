function encode(frase) {
  let newCode = '';
  for (index in frase){
    switch (frase[index]){
      case 'a':
        newCode += 1;
        break;
      case 'e':
        newCode += 2;
        break;
      case 'i':
        newCode += 3;
        break;
      case 'o':
        newCode += 4;
        break;
      case 'u':
        newCode += 5;
        break;
      default:
        newCode += frase[index];
    }
  }
  return newCode
}

