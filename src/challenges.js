function fizzBuzz(array) {
  let newArray = [];
  for (let index in array){
    if (array[index]%3 === 0 && array[index]%5 === 0){
      newArray[index] = 'fizzBuzz'
    }else if (array[index]%3 === 0){
      newArray[index] = 'fizz'
    }else if (array[index]%5 === 0){
      newArray[index] = 'buzz'
    } else {
      newArray[index] = 'bug!'
    }
  }
  return newArray
}

