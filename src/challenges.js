function highestCount(array) {
  let maiorNumero = 0;
  let cont = 0;
  for (let index in array){
    if (maiorNumero < array[index]){
      maiorNumero = array[index]
    }
  }
  for (let index2 in array){
    if (maiorNumero === array[index2]){
      cont += 1
    }
  }
  return cont
}

